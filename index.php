				public $text;
				public $answers = array();

				public function setQuestionText($text) {
					$this->text = $text;
				}

				public function getQuestionText() {
					return $this->text;
				}

				public function setAmountAnswers($amount) {
					$this->amountAnswers = $amount;
				}

				public function setAnswer($answer, $position) {
					$this->answers[$position] = $answer;
				}

				public function getAnswer($position) {
					return $this->answers[$position];
				}

				public function getAmount() {
					return sizeof($this->answers);
				}
			}

